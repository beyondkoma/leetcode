class NumArray(object):
    def __init__(self, nums):
        """
        initialize your data structure here.
        :type nums: List[int]
        """
        self.nums = nums
        self.index_sum = []
        count = 0
        for pos, val in enumerate(nums):
            count += val
            self.index_sum.append(count)

    def sumRange(self, i, j):
        """
        sum of elements nums[i..j], inclusive.
        :type i: int
        :type j: int
        :rtype: int
        """
        return self.index_sum(j) - self.index_sum(i) + self.nums[i]


